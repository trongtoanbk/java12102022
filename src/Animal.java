public class Animal {


    //1: Thuộc tính (Properties - Props)
    // thuộc tính: Xử lý thông tin cho đối tượng
    String name;
    String height;
    String weight;

    // 2: Phương thức khởi tạo
    public Animal(String name, String height, String weight){
        this.name = name;
        this.height = height;
        this.weight = weight;
    }

}
